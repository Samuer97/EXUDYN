void CMarkerNodeRotationCoordinate::ComputeMarkerData(const CSystemData& cSystemData, bool computeJacobian, MarkerData& markerData) const
{
	//A marker 'NodeCoordinate' must be attached to the ODE2 part of a node
	//always markerData.value is written ... also for velocities
	
	const CNodeODE2& cNode = (const CNodeODE2&)(*cSystemData.GetCNodes()[parameters.nodeNumber]);

	Index n = cNode.GetNumberOfODE2Coordinates(); //works already for both cases
	if (n) //access to GetCurrentCoordinateVector only if not ground node (which has no real coordinates, but it is allows to constrain to a ground node 'coordinate' being always zero
	{
		Matrix3D A = cNode.GetRotationMatrix();
		Vector3D rot = RigidBodyMath::RotationMatrix2RotXYZ(A); //compute tait bryan angles
		Real phi = rot[parameters.rotationCoordinate];			//get right coordinate
		Real omega = cNode.GetAngularVelocity()[parameters.rotationCoordinate];

		markerData.velocityAvailable = true;
		markerData.vectorValue.SetVector({ phi }); //for position level constraints
		markerData.vectorValue_t.SetVector({ omega }); //for velocity level constraints

		if (computeJacobian)
		{
			//zero-vector with 1 at parameters.coordinate => this is also done for velocity markers; this might not work for non-holonomic constraints
			cNode.GetRotationJacobian(markerData.rotationJacobian); //temporary!
			markerData.jacobian.SetNumberOfRowsAndColumns(1, n);    //only one coordinate acting!
			markerData.jacobian.SetSubmatrix(LinkedDataMatrix(markerData.rotationJacobian, parameters.rotationCoordinate, 1));
		}
	}
	else //ground node
	{
		markerData.velocityAvailable = true;
		//markerData.value = 0;
		//markerData.value_t = 0; //set this also for ground markers, because it could be asked for in connector!!!!
		markerData.vectorValue.SetVector({ 0. });   //indicates that value is zero
		markerData.vectorValue_t.SetVector({ 0. }); //set this also for ground markers, because it could be asked for in connector!!!!
		markerData.jacobian.SetNumberOfRowsAndColumns(0, 0); //this signals the connector/system not to apply any load or Lagrange multiplier
	}
}

