void CMarkerBodiesRelativeRotationCoordinate::ComputeMarkerData(const CSystemData& cSystemData, bool computeJacobian, MarkerData& markerData) const
{
	//compose jacobian from both bodies:
	//compute relative translation along coordinate
	//compose jacobian: initialize size = 1 x (nCoords0 + nCoords1)

	((CObjectBody*)(cSystemData.GetCObjects()[parameters.bodyNumbers[0]]))->ComputeRigidBodyMarkerData(parameters.localPosition0, computeJacobian, markerData);
	//Vector3D position0 = markerData.position;
	//Vector3D velocity0 = markerData.velocity;
	Matrix3D A0 = markerData.orientation;
	Vector3D angularVelocityLocal0 = markerData.angularVelocityLocal;
	Vector3D globalAxis0 = A0 * parameters.axis0;
	Vector3D globalAxis0_t = markerData.orientation * (angularVelocityLocal0.CrossProduct(parameters.axis0) );

	Index body0ODE2 = markerData.rotationJacobian.NumberOfColumns();
	Index body1ODE2 = cSystemData.GetCObjects()[parameters.bodyNumbers[1]]->GetODE2Size();

	if (computeJacobian)
	{
		markerData.jacobian.SetNumberOfRowsAndColumns(1, body0ODE2 + body1ODE2);
		markerData.jacobian.SetAll(0.);

		for (Index col = 0; col < body0ODE2; col++)
		{
			for (Index row = 0; row < EXUstd::dim3D; row++)
			{
				//markerData.rotationJacobian
				markerData.jacobian(0, col) -= markerData.rotationJacobian(row, col) * globalAxis0[row];
			}
		}
	}
	((CObjectBody*)(cSystemData.GetCObjects()[parameters.bodyNumbers[1]]))->ComputeRigidBodyMarkerData(parameters.localPosition1, computeJacobian, markerData);
	//Vector3D position1 = markerData.position;
	//Vector3D velocity1 = markerData.velocity;
	Matrix3D A1 = markerData.orientation;
	Vector3D angularVelocityLocal1 = markerData.angularVelocityLocal;

	if (computeJacobian)
	{
		for (Index col = 0; col < body1ODE2; col++)
		{
			for (Index row = 0; row < EXUstd::dim3D; row++)
			{
				markerData.jacobian(0, col + body0ODE2) += markerData.rotationJacobian(row, col) * globalAxis0[row];
			}
		}
	}

	Matrix3D Arel = A0.GetTransposed() * A1;
	Matrix3D Abasis = EXUmath::ComputeOrthogonalBasisX(parameters.axis0); //rotation axis = first column = local x-axis
	Matrix3D AbasisRel = Abasis.GetTransposed() * Arel * Abasis;

	Real value = atan2(AbasisRel(2, 1), AbasisRel(1, 1)) - parameters.offset; //angle in -pi..+pi
	//pout << "phi=" << value << "\n";
	//continuation:
	Index nodeNumber = GetNodeNumber();
	if (nodeNumber != EXUstd::InvalidIndex)
	{
		Real anglePrev = ((const CNodeData&)(cSystemData.GetCNode(nodeNumber))).GetCoordinateVector(ConfigurationType::StartOfStep)[0];
		value = EXUmath::ContinuousAngle(value, anglePrev);
	}

	Real value_t = globalAxis0 * (A1 * angularVelocityLocal1 - A0 * angularVelocityLocal0); //relative angular velocity around global axis0

	markerData.vectorValue.SetVector({ value }); //relative rotation
	markerData.vectorValue_t.SetVector({ value_t }); 
	markerData.velocityAvailable = true; //should already be set
}


